#Task 1: Compare two numbers using == and != and print the results.

#Method-1
print (5==5) #display result of 5==5
print (5!=5) #display result of 5!=5

#Method-2
equal = (5 == 5) #store value of 5==5 in equal(variable)
not_equal = (5!=5) #store value of 5!=5 in not_equal(variable)

#Display results 
print (f"For 5==5 system gives: {equal}")
print (f"For 5!=5 system gives: {not_equal}")