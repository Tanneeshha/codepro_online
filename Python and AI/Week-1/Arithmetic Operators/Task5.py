#Task 5: Write an expression that calculates the average of three numbers.

#Method-1
avg = (4 + 5 + 6)/3 #expression of adding 4,5 and 6 and dividing its result from 3 stored in avg(variable).
print (avg) #display avg

#Method-2
num1 = 4 #assign 4 to num1.
num2 = 5 #assign 5 to num2.
num3 = 6 #assign 6 to num3.
res = num1+num2+num3 #add values of num1, num2 and num3 and store in res(variable).
avg = res/3 #divide value of res by 3 and store in avg(variable).
print (avg) #display value of avg.

#Method-3
