#Task 3: Swap two variables without using a third variable.

#Store any value in two variable
val1 = 10 
val2 = 20 

#Display both values before swapping
print(f"Values before swapping are: 'Value 1' = {val1} and 'Value 2' = {val2}")

#Swapping values
val1 , val2 = val2 , val1

#Display both values after swapping
print(f"Values after swapping become: 'Value 1' = {val1} and 'Value 2' = {val2}")
